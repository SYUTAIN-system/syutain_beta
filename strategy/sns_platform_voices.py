def get_voice_guide(platform: str, account: str = "") -> dict:
    """プラットフォーム+アカウントに応じたボイスガイドを返す"""
    key = platform
    if platform == "x":
        if account in ("shimahara", "sima_daichi", "shima"):
            key = "x_shimahara"
        else:
            key = "x_syutain"
    return PLATFORM_VOICES.get(key, PLATFORM_VOICES["x_shimahara"])


def build_voice_prompt(platform: str, account: str = "") -> str:
    """ボイスガイドをLLMプロンプト用の文字列に変換"""
    guide = get_voice_guide(platform, account)
    lines = [
        f"## このプラットフォームのボイス",
        f"**ペルソナ**: {guide['persona']}",
        f"**トーン**: {guide['tone']}",
        f"**構造**: {guide['structure']}",
        f"",
        f"### ボイスルール",
    ]
    for rule in guide["voice_rules"]:
        lines.append(f"- {rule}")
    lines.append("")
    lines.append("### 良い例（このスタイルで書く）")
    for ex in guide["example_styles"][:3]:
        lines.append(f"- {ex}")
    lines.append("")
    lines.append("### 絶対禁止")
    for f in guide["forbidden"]:
        lines.append(f"- {f}")
    return "\n".join(lines)
