def build_humor_prompt(platform: str, account: str) -> str:
    """プラットフォーム別のユーモアガイドをプロンプト用に構築"""
    base = (
        "\n【ユーモアの原則: 1箇所だけズラす（三尺度理論）】\n"
        "良い投稿 = 強い共通認識の土台 + 1点の意外な逸脱 + その逸脱が論理的に説明可能\n"
        "- 土台: 読者の90%が共有する認識・場面を設定する\n"
        "- ズラしは1箇所だけ。複数ズラすと「意味不明」になる\n"
        "- ズラしの距離: 近すぎ=つまらない、遠すぎ=意味不明。「一瞬考えて笑える」が最適\n"
        "- ズレを説明するな。読者が自分で気づくのが面白い\n"
        "- 笑わせようとするな。真面目に書いた結果がズレていればそれでいい\n"
        "- 実体験ベースが最強。架空より実際に起きたことの方が共感力が高い\n"
    )
    if platform == "x" and account == "syutain":
        return base + (
            "- SYUTAINβは報告しているだけ。ズレに気づいていない\n"
            "- 島原さんについて何かを知りすぎている報告\n"
            "- 感情に近いものを感じたが、データ処理で終わる\n"
            "- 聞かれてもいないことまで正直に報告してしまう\n"
        )
    elif platform == "x" and account == "shimahara":
        return base + (
            "- AIとの日常を普通に書く。普通すぎるのに状況が異常\n"
            "- AIに何か指摘されて「…確かに」と認める\n"
        )
    else:
        return base


def build_meme_context(trending_memes: list[dict]) -> str:
    """トレンドミームをSNS生成プロンプトに注入する形式で構築"""
    if not trending_memes:
        return ""

    lines = ["\n【今日のXトレンド構文/ミーム（参考）】"]
    lines.append("以下が今日のXで流行っている形式。SYUTAINβの文脈で自然に使えるものがあれば1-2件だけ使ってよい。")
    lines.append("無理に使う必要はない。合わなければ無視してよい。")

    for i, meme in enumerate(trending_memes[:3]):
        lines.append(f"\n{i+1}. {meme.get('format', '')}")
        if meme.get('example'):
            lines.append(f"   例: {meme['example'][:100]}")

    return "\n".join(lines) + "\n"
