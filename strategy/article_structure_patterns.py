# ===== パターン自動選択ロジック =====

def select_best_pattern(theme: str, detected_genre: str = "") -> dict:
    """テーマのキーワードに基づいて最適な記事構成パターンを選択する。

    Returns:
        dict: 選択されたパターン（ARTICLE_PATTERNS の1エントリ）
    """
    theme_lower = theme.lower() if theme else ""

    # 各パターンのスコアを計算（キーワードマッチ数）
    scores = {}
    for key, pattern in ARTICLE_PATTERNS.items():
        score = sum(1 for kw in pattern["keywords"] if kw in theme_lower)
        # ジャンルとの相性ボーナス
        if detected_genre in ("system_building", "ai_tech") and key in ("design_philosophy", "comparison", "numbers_first"):
            score += 1
        if detected_genre in ("non_engineer", "build_in_public") and key in ("failure_to_recovery", "timeline_documentary", "confession"):
            score += 1
        scores[key] = score

    # 最高スコアのパターンを選択（同点なら最高スコアの中からランダム）
    max_score = max(scores.values())
    if max_score > 0:
        best_keys = [k for k, v in scores.items() if v == max_score]
        selected_key = random.choice(best_keys)
    else:
        # キーワードマッチがない場合、Build in Public向けパターンからランダム
        bip_patterns = ["failure_to_recovery", "timeline_documentary", "numbers_first", "confession", "lesson_list"]
        selected_key = random.choice(bip_patterns)

    selected = ARTICLE_PATTERNS[selected_key]
    logger.info(f"記事構成パターン選択: {selected['name']} (score={scores.get(selected_key, 0)}, theme={theme[:30]})")
    return selected


def build_pattern_prompt(pattern: dict) -> str:
    """選択されたパターンからLLMへの構成案生成プロンプトを構築する。"""
    sections_text = ""
    total_chars = 0
    for i, sec in enumerate(pattern["sections"], 1):
        sections_text += f"\n{i}. **{sec['name']}**（約{sec['chars']}字）\n"
        sections_text += f"   目的: {sec['purpose']}\n"
        sections_text += f"   技法: {sec['technique']}\n"
        total_chars += sec["chars"]

    prompt = (
        f"## 記事構成パターン: {pattern['name']}\n\n"
        f"{pattern['prompt_fragment']}\n\n"
        f"## 感情曲線\n{pattern['emotion_curve']}\n\n"
        f"## セクション構成（目標合計 {total_chars}字）\n"
        f"{sections_text}\n\n"
        f"上記の構成に従って、各セクションの具体的な内容を計画してください。\n"
        f"各セクションに書く具体的なエピソード・数値・ポイントを2-3行で記述してください。"
    )
    return prompt
